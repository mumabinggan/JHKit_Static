#import "JHLabel.h"
#import "JHMacro.h"

@implementation JHLabel

- (id)init {
    self = [super init];
    if (self) {
        self.backgroundColor = kClearColor;
    }
    return self;
}

- (id)initWithFrame:(CGRect)frame {
    self = [super initWithFrame:frame];
    if (self) {
        self.backgroundColor = kClearColor;
    }
    return self;
}

@end
